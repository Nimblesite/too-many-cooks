// Generates src/migrations.gen.ts by embedding all prisma/migrations/*/migration.sql files.
// Run via the `prebuild` and `pretest` npm scripts.

import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const MIGRATIONS_DIR = resolve(HERE, "..", "prisma", "migrations");
const OUTPUT = resolve(HERE, "..", "src", "migrations.gen.ts");

const isMigrationDir = (entry) => {
  const full = join(MIGRATIONS_DIR, entry);
  if (!statSync(full, { throwIfNoEntry: false })?.isDirectory()) { return false; }
  return statSync(join(full, "migration.sql"), { throwIfNoEntry: false })?.isFile() === true;
};

const names = readdirSync(MIGRATIONS_DIR).filter(isMigrationDir).sort();

const entries = names.map((name) => ({
  name,
  sql: readFileSync(join(MIGRATIONS_DIR, name, "migration.sql"), "utf-8"),
}));

const body = entries
  .map(({ name, sql }) => `  { name: ${JSON.stringify(name)}, sql: ${JSON.stringify(sql)} },`)
  .join("\n");

const output = `/// Generated by scripts/gen-migrations.mjs — do not edit.
/// Embeds Prisma migration SQL so the published package is self-contained.

export type Migration = { readonly name: string; readonly sql: string };

export const MIGRATIONS: readonly Migration[] = [
${body}
];
`;

writeFileSync(OUTPUT, output);
console.error(`Wrote ${String(entries.length)} migration(s) to ${OUTPUT}`);
